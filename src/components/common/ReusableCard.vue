<template>
  <div class="row justify-content-center">
    <div class="col-12">
      <div class="card reusable-card" :style="cardStyle">
        
        <!-- Card Header -->
        <div class="card-header fancy-3d-header enhanced-header" :style="headerStyle">
          <div class="header-background-animation"></div>
          <div class="header-particles">
            <span class="particle" v-for="n in 5" :key="n"></span>
          </div>
          <h3 class="header-title-enhanced" :style="titleStyle">
            <!-- Icon Resolver Support -->
            <span v-if="iconName && iconData" class="title-icon icon-wrapper-lg">
              <i v-if="iconData.type === 'devicon'" :class="deviconClass + ' icon-lg'"></i>
              <img v-else-if="iconData.type === 'local'" :src="iconData.src" :alt="iconData.alt" class="icon-img-lg" />
              <span v-else class="icon-lg">{{ iconData.src }}</span>
            </span>
            <!-- Fallback to emoji if iconName provided but no iconData found -->
            <span v-else-if="iconName" class="title-icon">{{ icon }}</span>
            <!-- Fallback to emoji -->
            <span v-else-if="extractedIcon" class="title-icon">{{ extractedIcon }}</span>
            
            <span class="title-text">{{ extractedTitle }}</span>
            <div class="title-underline"></div>
          </h3>
          <div class="header-glow-effect"></div>
        </div>

        <!-- Card Body -->
        <div class="card-body" :style="bodyStyle">
          <slot></slot>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { resolveIcon, getDeviconClass } from '../../utils/iconResolver.js'

export default {
  name: 'ReusableCard',
  props: {
    title: {
      type: String,
      required: true
    },
    icon: {
      type: String,
      default: 'ðŸ“‹'
    },
    iconName: {
      type: String,
      default: null
    },
    cardPadding: {
      type: String,
      default: '20px'
    },
    bodyPadding: {
      type: String,
      default: '0 10px'
    }
  },
  data() {
    return {
      resolvedIconData: null
    }
  },
  mounted() {
    // Resolve icon if iconName is provided
    if (this.iconName) {
      this.resolvedIconData = resolveIcon(this.iconName)
    }
  },
  computed: {
    // Extract emoji from title and separate icon from text
    extractedIcon() {
      // If iconName is provided, return null (will be handled in template)
      if (this.iconName) {
        return null
      }
      
      // If a custom icon is provided, use it
      if (this.icon !== 'ðŸ“‹') {
        return this.icon
      }
      
      // Otherwise, try to extract emoji from title
      const emojiRegex = /^[\u{1F600}-\u{1F64F}]|[\u{1F300}-\u{1F5FF}]|[\u{1F680}-\u{1F6FF}]|[\u{1F1E0}-\u{1F1FF}]|[\u{2600}-\u{26FF}]|[\u{2700}-\u{27BF}]/u
      const match = this.title.match(emojiRegex)
      return match ? match[0] : 'ðŸ“‹'
    },
    extractedTitle() {
      // If a custom icon is provided, use the full title
      if (this.icon !== 'ðŸ“‹') {
        return this.title
      }
      
      // Otherwise, remove emoji from title
      const emojiRegex = /^[\u{1F600}-\u{1F64F}]|[\u{1F300}-\u{1F5FF}]|[\u{1F680}-\u{1F6FF}]|[\u{1F1E0}-\u{1F1FF}]|[\u{2600}-\u{26FF}]|[\u{2700}-\u{27BF}]/u
      return this.title.replace(emojiRegex, '').trim()
    },
    iconData() {
      if (this.iconName && this.resolvedIconData) {
        return this.resolvedIconData
      }
      return null
    },
    deviconClass() {
      if (this.iconData && this.iconData.type === 'devicon') {
        return getDeviconClass(this.iconData.src)
      }
      return null
    },
    cardStyle() {
      return {
        background: 'linear-gradient(135deg, rgba(40, 10, 80, 0.15) 0%, rgba(30, 5, 60, 0.1) 50%, rgba(20, 0, 40, 0.05) 100%)',
        backdropFilter: 'blur(10px)',
        border: '1px solid rgba(80, 30, 140, 0.3)',
        borderRadius: '20px',
        boxShadow: '0 20px 50px rgba(40, 10, 80, 0.2)',
        padding: this.cardPadding
      }
    },
    headerStyle() {
      return {
        margin: '-10px -10px 30px -10px',
        padding: '25px 40px',
        borderRadius: '20px 20px 0 0',
        border: 'none'
      }
    },
    titleStyle() {
      return {
        color: 'white',
        margin: '0',
        fontWeight: '700',
        textAlign: 'center',
        position: 'relative',
        zIndex: '10'
      }
    },
    bodyStyle() {
      return {
        padding: this.bodyPadding
      }
    }
  }
}
</script>

