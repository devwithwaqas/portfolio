<script>
import ReusableCard from './ReusableCard.vue'
import { ANIMATION_CONFIG } from '../config/constants.js'

export default {
  name: 'Skills',
  components: {
    ReusableCard
  },
  data() {
    return {
      // VUE PROPER: Track hover states reactively
      hoveredCards: new Set(),
      hoverThrottle: new Map()
    }
  },
  mounted() {
    // VUE MANAGEMENT: Move hover optimization from global main.js to component
    // Ensure DOM is ready before setting up hover management
    this.$nextTick(() => {
      this.setupCardHoverManagement()
    })
  },
  beforeUnmount() {
    // VUE LIFE-CYCLE: Remove listeners when component is destroyed
    this.cleanupCardHoverListeners()
  },
  methods: {
    setupCardHoverManagement() {
      // VUE COMPONENT: Use DOM query within this component scope per Vue best practices 
      this.$nextTick(() => {
        // SAFETY CHECK: Ensure $el exists and is a DOM element
        if (this.$el && this.$el.querySelectorAll) {
          const cyberCards = this.$el.querySelectorAll('.cyber-container')
          cyberCards.forEach(card => {
            card.addEventListener('mouseenter', this.handleCardHoverStart)
            card.addEventListener('mouseleave', this.handleCardHoverEnd)
          })
        }
      })
    },
    handleCardHoverStart(event) {
      // VUE REACTIVE: Use requestAnimationFrame for smooth hover
      requestAnimationFrame(() => {
        const container = event.currentTarget
        this.hoveredCards.add(container)
        
        // Apply animation optimizations
        this.optimizeHoverStart(container)
      })
    },
    handleCardHoverEnd(event) {
      requestAnimationFrame(() => {
        const container = event.currentTarget
        this.hoveredCards.delete(container)
        
        // Reset animation optimizations
        this.optimizeHoverEnd(container)
      })
    },
    optimizeHoverStart(card) {
      // VATIVE LOGIC: Copy optimizeStart from main.js moved here
      const trackers = card.querySelectorAll('.cyber-tracker')
      const glows = card.querySelectorAll('.cyber-glow-1, .cyber-glow-2, .cyber-glow-3')
      
      trackers.forEach(tracker => {
        tracker.style.willChange = 'transform'
      })
      
      glows.forEach(glow => {
        glow.style.willChange = 'opacity, transform, filter'
      })
    },
    optimizeHoverEnd(card) {
      // VATIVE LOGIC: Copy optimizeEnd from main.js moved here
      const trackers = card.querySelectorAll('.cyber-tracker')
      const glows = card.querySelectorAll('.cyber-glow-1, .cyber-glow-2, .cyber-glow-3')
      
      trackers.forEach(tracker => {
        tracker.style.willChange = 'auto'
      })
      
      glows.forEach(glow => {
        glow.style.willChange = 'auto'
      })
    },
    cleanupCardHoverListeners() {
      // VUE LIFECYCLE: Remove event listeners on unmount
      // VUE LIFECYCLE: Clean up DOM listeners by querying component container
      if (this.$el && this.$el.querySelectorAll) {
        const cyberCards = this.$el.querySelectorAll('.cyber-container')
        cyberCards.forEach(card => {
          card.removeEventListener('mouseenter', this.handleCardHoverStart)
          card.removeEventListener('mouseleave', this.handleCardHoverEnd)
        })
      }
    }
  }
}
